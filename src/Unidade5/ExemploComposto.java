package Unidade5;

public class ExemploComposto {
    public static void main(String[] args) {
        for(int i = 0; i <= 10; i++){
            for(int j = 10; j >= 0; j--){
                System.out.println("i = "+i);
                System.out.println("j = "+j);
            }
        }
    }

}
